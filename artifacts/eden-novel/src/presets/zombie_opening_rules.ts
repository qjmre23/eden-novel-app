const zombie_opening_rules = `
━━━ ZOMBIE / OUTBREAK OPENING RULES ━━━

THE WORLD HAS ALREADY CHANGED:
• The zombie apocalypse is not something that is coming — it is already here, or breaking out NOW.
• If the outbreak is just beginning: show the exact moment the MC realizes something is horribly wrong. Before vs. After must be felt in real time.
• If the world has already fallen: show a world that used to be alive but is now hollow and dangerous.

SOUND AND SILENCE:
• Sound design is essential. Wrong sounds (moaning, dragging), alarming silences, distant screams — all convey the threat before it is seen.
• Every noise the MC makes matters. Stealth is survival.

HUMAN THREAT AS REAL AS INFECTED:
• From the opening, establish that other survivors may be as dangerous as the infected.
• Trust is a resource, as scarce as food.

FORBIDDEN IN ZOMBIE OPENINGS:
✗ Extended peaceful normalcy with no sign of threat
✗ The MC feeling confident and in control of their environment
✗ Infected that are easily ignored or not dangerous

REQUIRED IN ZOMBIE OPENINGS:
✓ Sensory detail of the ruined/dangerous world
✓ An immediate survival calculation: where to go, what's near, what's safe
✓ The MC's emotional weight — grief, numbness, or hardened resolve
✓ One concrete zombie encounter or sign of threat in the opening scene
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
`;

export default zombie_opening_rules;
