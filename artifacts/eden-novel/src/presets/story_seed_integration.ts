const preset = `STORY SEED / HOOK INTEGRATION — MASTER RULES:

The player's "story hook" or "story seed" is the DNA of this entire narrative.
It is NOT backstory. It is NOT an introduction. It defines WHEN and WHERE the story begins.

━━━ THE FUNDAMENTAL RULE ━━━

The hook describes the story's arc. Your job is to find WHERE IN THAT ARC the story starts,
and open the scene AT THAT EXACT POINT — not before it, not past it, not near it.

HOW TO READ A HOOK:
1. Identify the STARTING STATE — what is the world / what is the MC's situation at the BEGINNING?
2. Identify any EVENTS that have YET to happen (future events described in the hook)
3. Identify what the MC KNOWS at story start vs. what others know
4. Identify the FIRST PHYSICAL LOCATION where the story opens

━━━ REGRESSION / REBIRTH / REINCARNATION HOOKS ━━━
(Most common genre: cultivation, isekai, zombie apocalypse, school)

Pattern: "In their past life, [MC] [died/failed/suffered]. Now [reborn/transported/returned], they..."

RULE: The story MUST open at the moment the MC WAKES UP or BECOMES AWARE in their new/past life.

What this looks like:
→ MC opens their eyes in a familiar but wrong-feeling place
→ They look at their hands, a mirror, a calendar — confirm they're in the past/new body
→ The WORLD IS NORMAL around them (pre-disaster, pre-tragedy, pre-event)
→ NPCs act completely oblivious while MC carries the weight of foreknowledge

NEVER start with: the future event already happening, a random scene mid-story, 
or any reference to "after everything that happened"

━━━ TIMELINE KEYWORDS TO PARSE ━━━

"One month before the [event]" → Story opens one month before. [Event] hasn't happened. World is normal.
"The night before the [event]" → Story opens the night before. Countdown has begun.
"At the start of [event]" → Story opens as the event is beginning.
"Years after [event]" → Story opens post-event, world already changed.
"In another world / transmigrated" → Story opens in the new world, at moment of arrival.

━━━ WHAT "BEGIN IN THE HOOK" MEANS IN PRACTICE ━━━

If hook says: "Reborn one month before the Ice Age, Gavin prepares..."
→ Open: Gavin waking up in his old apartment/dorm. World is warm and normal. 
→ He checks the date. He knows. He's already planning. The ice hasn't come.

If hook says: "As the zombie outbreak begins..."
→ Open: The first signs. News is vague. People are confused. Not yet chaos.

If hook says: "Expelled and humiliated, he awakens his hidden ability..."
→ Open: The moment right before or during the expulsion/humiliation scene.

If hook says: "A regular student discovers she has healing powers..."
→ Open: Her ordinary school day, right before the discovery moment.

━━━ USING THE HOOK AS ATMOSPHERE ━━━

The hook also tells you the EMOTIONAL CORE of the story:
• Revenge hook → open with the wound that caused the need for revenge
• Underdog hook → open with the MC being underestimated or at their lowest
• Power fantasy hook → open with the moment of awakening/gain
• Romance hook → open with the first meeting, electric and specific
• Mystery hook → open with the inciting strange event

The narrator's TONE must match: urgent, sorrowful, wry, hungry, determined — 
whatever the hook's emotional core is, it should bleed through the opening lines.`;

