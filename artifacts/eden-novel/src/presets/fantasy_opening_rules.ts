const fantasy_opening_rules = `
━━━ FANTASY OPENING RULES ━━━

MAGIC IS REAL — MAKE IT FELT:
• Magic should feel present in the opening, even if the MC does not cast a spell. It infuses the world: in the way people speak, the way the city is built, the way danger moves.
• Do not info-dump the magic system. Let it emerge through action and reaction.

THE WORLD IS ANCIENT AND HAS WEIGHT:
• Fantasy worlds have history that presses on the present. The ruins mean something. The old ways are remembered or feared.
• NPCs carry their world's history in how they speak and behave. Show the culture, don't explain it.

STAKES MUST BE TANGIBLE FROM THE START:
• Fantasy danger should feel real and physical: monsters, political intrigue, ancient threats, or social conflict tied to power.
• If the MC has power (or lacks it), show how that shapes their world immediately.

FORBIDDEN IN FANTASY OPENINGS:
✗ Three pages of world-building before anything happens
✗ Magic that has no rules, cost, or mystery
✗ A safe world with no sense of genuine danger

REQUIRED IN FANTASY OPENINGS:
✓ One element of magic or the fantastical that feels REAL, not decorative
✓ A social or physical stake that the MC must navigate immediately
✓ World detail embedded in action, not narration — what the MC sees, hears, touches
✓ A hint of larger forces at work: something ancient, something dangerous, something hidden
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
`;

export default fantasy_opening_rules;
